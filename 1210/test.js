function 덧셈(i, ii){
	console.log(i, " + " + ii, " = ", (i + ii));
	return (i + ii);
}

function 뺄셈(i, ii){
	console.log(i, " - ", ii, " = ", (i - ii));
	return (i - ii);
}

function 곱셈(i, ii){
	console.log(i, " * ", ii, " = ", (i * ii));
	return (i * ii);
}

function 나눗셈(i, ii){
	console.log(i, " / ", ii, " = ", (i / ii));
	return (i / ii);
}

console.log(덧셈(1,2), 뺄셈(1,2), 곱셈(1,2), 나눗셈(1,2));
